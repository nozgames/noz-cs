namespace NoZ.Editor;

public static class AssetManifest
{
    public static bool IsModified { get; set; }

    public static void Generate(bool force=false)
    {
        if (!force && !IsModified)
            return;

        IsModified = false;

        var config = EditorApplication.Config;

        if (config.GenerateCs != null)
            GenerateCs(config);

        if (config.GenerateLua != null)
            GenerateLua(config);

        Project.DocumentAdded += doc => { IsModified = true; };
    }

    private static string GetAssetTypeName(AssetType type)
        => Asset.GetDef(type)?.Name ?? type.ToString();

    // Lower = loaded earlier. Vfx and Scene reference sprites at Load time, so they must load after.
    private static int GetLoadPriority(AssetType type)
    {
        if (type == AssetType.Vfx) return 99;
        if (type == AssetType.Scene) return 100;
        return 0;
    }

    private static string GetAssetTypeExpr(AssetType type)
    {
        var def = Asset.GetDef(type);
        if (def == null)
            return $"AssetType.FromString(\"{type}\")";

        // Check if AssetType has a built-in static field for this type
        var field = typeof(AssetType).GetField(def.Name, System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Static);
        if (field != null)
            return $"AssetType.{def.Name}";

        // Custom asset type: use the runtime type's Type field
        return $"{def.RuntimeType.Name}.Type";
    }

    private static string Pluralize(string typeName)
    {
        if (typeName.EndsWith("s", StringComparison.Ordinal))
            return typeName + "es";
        return typeName + "s";
    }

    private static List<(AssetType Type, Document Doc)> GetManifestEntries()
    {
        var entries = new List<(AssetType Type, Document Doc)>();

        // Collect assets owned by bundles and their transitive dependencies
        var bundleOwned = new HashSet<(AssetType, string)>();
        var depQueue = new Queue<(AssetType, string)>();
        foreach (var doc in Project.Documents)
        {
            if (doc is BundleDocument bundle)
            {
                foreach (var entry in bundle.Entries)
                    depQueue.Enqueue((entry.AssetType, entry.AssetName));
            }
        }

        var deps = new List<(AssetType Type, string Name)>();
        while (depQueue.Count > 0)
        {
            var dep = depQueue.Dequeue();
            if (!bundleOwned.Add(dep))
                continue;

            var depDoc = Project.Find(dep.Item1, dep.Item2);
            if (depDoc == null)
                continue;

            if (!depDoc.Loaded)
                depDoc.Load();

            deps.Clear();
            depDoc.GetDependencies(deps);
            foreach (var d in deps)
                depQueue.Enqueue(d);
        }

        // Collect actual sprite names for conflict detection
        var spriteNames = new HashSet<string>();
        foreach (var doc in Project.Documents)
        {
            if (!doc.ShouldExport) continue;
            if (doc.Def.Type == AssetType.Sprite)
                spriteNames.Add(doc.Name);
        }

        foreach (var doc in Project.Documents)
        {
            if (!doc.ShouldExport) continue;

            // Skip assets owned by a bundle
            if (bundleOwned.Contains((doc.Def.Type, doc.Name)))
                continue;

            // Skip remote bundles
            if (doc is BundleDocument { IsRemote: true })
                continue;

            // Atlas assets are produced by AtlasManager, not from documents.
            if (doc.Def.Type == AssetType.Atlas)
                continue;

            entries.Add((doc.Def.Type, doc));
        }

        return entries;
    }

    private static void GenerateCs(EditorConfig config)
    {
        var path = Path.Combine(Project.Path, config.GenerateCs!);
        var dir = Path.GetDirectoryName(path);
        if (!string.IsNullOrEmpty(dir))
            Directory.CreateDirectory(dir);

        var manifestEntries = GetManifestEntries();

        using var writer = new StreamWriter(File.Create(path));

        writer.WriteLine("//");
        writer.WriteLine("//  Auto-generated by NoZ Editor - DO NOT EDIT");
        writer.WriteLine("//");
        writer.WriteLine();
        writer.WriteLine("using NoZ;");

        // Emit using directives for namespaces of custom asset runtime types
        var namespaces = manifestEntries
            .Select(e => Asset.GetDef(e.Type)?.RuntimeType.Namespace)
            .Where(ns => ns != null && ns != "NoZ" && ns != config.CsNamespace)
            .Distinct()
            .OrderBy(ns => ns);
        foreach (var ns in namespaces)
            writer.WriteLine($"using {ns};");

        writer.WriteLine();
        writer.WriteLine($"namespace {config.CsNamespace};");
        writer.WriteLine();
        writer.WriteLine($"public static class {config.CsClass}");
        writer.WriteLine("{");
        var documentsByType = manifestEntries
            .GroupBy(e => e.Type)
            .OrderBy(g => GetLoadPriority(g.Key))
            .ThenBy(g => g.Key.ToString())
            .Select(g => new { Key = g.Key, Docs = g.Select(e => e.Doc).ToList() })
            .ToList();

        // We emit a single Atlas asset whenever there are exported sprites.
        var hasSprites = documentsByType.Any(g => g.Key == AssetType.Sprite);
        var atlasName = AtlasManager.GameAtlasName;
        var atlasPascal = ToPascalCase(atlasName);

        // Names class (deduplicated since different types can share names)
        writer.WriteLine("    public static class Names");
        writer.WriteLine("    {");
        var uniqueNames = documentsByType
            .SelectMany(g => g.Docs)
            .Select(d => d.Name)
            .Concat(hasSprites ? new[] { atlasName } : Enumerable.Empty<string>())
            .Distinct()
            .OrderBy(n => n);
        foreach (var name in uniqueNames)
        {
            var constName = ToPascalCase(name);
            writer.WriteLine($"        public const string {constName} = \"{name}\";");
        }
        writer.WriteLine("    }");

        // Layers class for sprite layer constants
        var sortOrders = config.SortOrders;
        if (sortOrders.Length > 0)
        {
            writer.WriteLine();
            writer.WriteLine("    public static class SortOrders");
            writer.WriteLine("    {");
            foreach (var so in sortOrders.OrderBy(s => s.SortOrder))
            {
                var constName = ToCSharpIdentifier(so.Id);
                writer.WriteLine($"        public const ushort {constName} = {so.SortOrder};");
            }
            writer.WriteLine("    }");
        }

        // Synthesized Atlases group (single atlas asset shared by all sprites)
        if (hasSprites)
        {
            writer.WriteLine();
            writer.WriteLine("    public static class Atlases");
            writer.WriteLine("    {");
            writer.WriteLine($"        public static readonly Atlas {atlasPascal} = new();");
            writer.WriteLine();
            writer.WriteLine("        public static void Load()");
            writer.WriteLine("        {");
            writer.WriteLine($"            {atlasPascal}.Load(Names.{atlasPascal});");
            writer.WriteLine("        }");
            writer.WriteLine();
            writer.WriteLine("        public static void Reload()");
            writer.WriteLine("        {");
            writer.WriteLine($"            {atlasPascal}.Reload();");
            writer.WriteLine("        }");
            writer.WriteLine();
            writer.WriteLine("        public static void Unload()");
            writer.WriteLine("        {");
            writer.WriteLine($"            {atlasPascal}.Dispose();");
            writer.WriteLine("        }");
            writer.WriteLine("    }");
        }

        // Static classes grouped by type, each with Load() and Unload() methods
        foreach (var group in documentsByType)
        {
            var typeName = GetAssetTypeName(group.Key);
            var pluralName = Pluralize(typeName);
            var runtimeType = Asset.GetDef(group.Key)?.RuntimeType.Name ?? "Asset";
            var assetTypeExpr = GetAssetTypeExpr(group.Key);
            var orderedDocs = group.Docs.OrderBy(d => d.Name).ToList();

            writer.WriteLine();
            writer.WriteLine($"    public static class {pluralName}");
            writer.WriteLine("    {");

            // Pre-allocated asset fields
            foreach (var doc in orderedDocs)
            {
                var fieldName = ToPascalCase(doc.Name);
                writer.WriteLine($"        public static readonly {runtimeType} {fieldName} = new();");
            }

            // Load method
            writer.WriteLine();
            var isSprite = group.Key == AssetType.Sprite;
            writer.WriteLine(isSprite
                ? "        public static void Load(Atlas atlas)"
                : "        public static void Load()");
            writer.WriteLine("        {");
            foreach (var doc in orderedDocs)
            {
                var fieldName = ToPascalCase(doc.Name);
                writer.WriteLine(isSprite
                    ? $"            {fieldName}.Load(Names.{fieldName}, atlas);"
                    : $"            {fieldName}.Load(Names.{fieldName});");
            }
            writer.WriteLine("        }");

            // Reload method
            writer.WriteLine();
            writer.WriteLine("        public static void Reload()");
            writer.WriteLine("        {");
            foreach (var doc in orderedDocs)
            {
                var fieldName = ToPascalCase(doc.Name);
                writer.WriteLine($"            {fieldName}.Reload();");
            }
            writer.WriteLine("        }");

            // Unload method
            writer.WriteLine();
            writer.WriteLine("        public static void Unload()");
            writer.WriteLine("        {");
            foreach (var doc in orderedDocs)
            {
                var fieldName = ToPascalCase(doc.Name);
                writer.WriteLine($"            {fieldName}.Dispose();");
            }
            writer.WriteLine("        }");

            writer.WriteLine("    }");
        }

        // Bones class — index constants per skeleton bone
        var skeletonGroup = documentsByType.FirstOrDefault(g => g.Key == AssetType.Skeleton);
        if (skeletonGroup != null && skeletonGroup.Docs.Count > 0)
        {
            var skeletons = skeletonGroup.Docs
                .OrderBy(d => d.Name)
                .Cast<SkeletonDocument>()
                .ToList();

            foreach (var skel in skeletons)
            {
                if (!skel.Loaded)
                    skel.Load();
            }

            var skeletonsWithBones = skeletons.Where(s => s.BoneCount > 0).ToList();
            if (skeletonsWithBones.Count > 0)
            {
                writer.WriteLine();
                writer.WriteLine("    public static class Bones");
                writer.WriteLine("    {");
                for (var s = 0; s < skeletonsWithBones.Count; s++)
                {
                    var skel = skeletonsWithBones[s];
                    if (s > 0) writer.WriteLine();
                    writer.WriteLine($"        public static class {ToPascalCase(skel.Name)}");
                    writer.WriteLine("        {");
                    for (var i = 0; i < skel.BoneCount; i++)
                    {
                        var boneName = ToPascalCase(skel.Bones[i].Name);
                        writer.WriteLine($"            public const int {boneName} = {i};");
                    }
                    writer.WriteLine("        }");
                }
                writer.WriteLine("    }");
            }

            var skeletonsWithStates = skeletons.Where(s => s.StateCount > 0).ToList();
            if (skeletonsWithStates.Count > 0)
            {
                writer.WriteLine();
                writer.WriteLine("    public static class SkeletonStates");
                writer.WriteLine("    {");
                for (var s = 0; s < skeletonsWithStates.Count; s++)
                {
                    var skel = skeletonsWithStates[s];
                    if (s > 0) writer.WriteLine();
                    writer.WriteLine($"        public static class {ToPascalCase(skel.Name)}");
                    writer.WriteLine("        {");
                    for (var i = 0; i < skel.StateCount; i++)
                    {
                        var stateName = ToPascalCase(skel.States[i].Name);
                        writer.WriteLine($"            public const int {stateName} = {i};");
                    }
                    writer.WriteLine("        }");
                }
                writer.WriteLine("    }");
            }
        }

        // Palettes class with expanded colors
        var palettes = PaletteManager.Palettes;
        if (palettes.Count > 0)
        {
            writer.WriteLine();
            writer.WriteLine("    public static class Palettes");
            writer.WriteLine("    {");

            for (int p = 0; p < palettes.Count; p++)
            {
                var palette = palettes[p];
                var paletteName = ToPascalCase(palette.Id);

                if (p > 0) writer.WriteLine();
                writer.WriteLine($"        public static class {paletteName}");
                writer.WriteLine("        {");

                var usedNames = new HashSet<string>();
                for (int c = 0; c < palette.Count; c++)
                {
                    var color = palette.Colors[c];
                    var r = (byte)(color.R * 255f);
                    var g = (byte)(color.G * 255f);
                    var b = (byte)(color.B * 255f);
                    var a = (byte)(color.A * 255f);

                    var colorName = palette.ColorNames[c];
                    var fieldName = !string.IsNullOrEmpty(colorName)
                        ? ToPascalCase(colorName)
                        : $"Color{c}";

                    // Handle duplicate names by appending index
                    if (!usedNames.Add(fieldName))
                        fieldName = $"{fieldName}{c}";

                    writer.WriteLine($"            public static readonly Color {fieldName} = new({r}, {g}, {b}, {a});");
                }

                writer.WriteLine("        }");
            }

            writer.WriteLine("    }");
        }

        // LoadAssets method
        writer.WriteLine();
        writer.WriteLine("    public static void LoadAssets()");
        writer.WriteLine("    {");

        if (hasSprites)
        {
            writer.WriteLine("        Atlases.Load();");
            writer.WriteLine();
        }

        foreach (var group in documentsByType)
        {
            var pluralName = Pluralize(GetAssetTypeName(group.Key));
            if (hasSprites && group.Key == AssetType.Sprite)
                writer.WriteLine($"        {pluralName}.Load(Atlases.{atlasPascal});");
            else
                writer.WriteLine($"        {pluralName}.Load();");
        }

        writer.WriteLine("    }");

        // ReloadAssets method
        writer.WriteLine();
        writer.WriteLine("    public static void ReloadAssets()");
        writer.WriteLine("    {");
        if (hasSprites)
            writer.WriteLine("        Atlases.Reload();");
        foreach (var group in documentsByType)
        {
            var pluralName = Pluralize(GetAssetTypeName(group.Key));
            writer.WriteLine($"        {pluralName}.Reload();");
        }
        writer.WriteLine("    }");

        // UnloadAssets method
        writer.WriteLine();
        writer.WriteLine("    public static void UnloadAssets()");
        writer.WriteLine("    {");
        foreach (var group in documentsByType)
        {
            var pluralName = Pluralize(GetAssetTypeName(group.Key));
            writer.WriteLine($"        {pluralName}.Unload();");
        }
        if (hasSprites)
            writer.WriteLine("        Atlases.Unload();");
        writer.WriteLine("    }");

        writer.WriteLine("}");

        Log.Info($"Generated {path}");
    }

    private static void GenerateLua(EditorConfig config)
    {
        var path = Path.Combine(Project.Path, config.GenerateLua!);
        var className = config.LuaClass;

        var dir = Path.GetDirectoryName(path);
        if (!string.IsNullOrEmpty(dir))
            Directory.CreateDirectory(dir);

        using var writer = new StreamWriter(File.Create(path));

        writer.WriteLine("--");
        writer.WriteLine("--  Auto-generated by NoZ Editor - DO NOT EDIT");
        writer.WriteLine("--");
        writer.WriteLine();
        writer.WriteLine($"local {className} = {{}}");
        writer.WriteLine();

        var manifestEntries = GetManifestEntries();
        var documentsByType = manifestEntries
            .GroupBy(e => e.Type)
            .OrderBy(g => GetLoadPriority(g.Key))
            .ThenBy(g => g.Key.ToString())
            .Select(g => new { Key = g.Key, Docs = g.Select(e => e.Doc).ToList() })
            .ToList();

        foreach (var group in documentsByType)
        {
            var typeName = GetAssetTypeName(group.Key);
            var pluralName = Pluralize(typeName);
            writer.WriteLine($"{className}.{pluralName} = {{");

            foreach (var doc in group.Docs.OrderBy(d => d.Name))
            {
                var constName = ToPascalCase(doc.Name);
                writer.WriteLine($"    {constName} = \"{doc.Name}\",");
            }

            writer.WriteLine("}");
            writer.WriteLine();
        }

        writer.WriteLine($"return {className}");

        Log.Info($"Generated {path}");
    }

    private static string ToPascalCase(string name)
    {
        var sb = new System.Text.StringBuilder();
        var capitalizeNext = true;

        foreach (var c in name)
        {
            if (c == '_' || c == '-' || c == ' ')
            {
                capitalizeNext = true;
            }
            else if (capitalizeNext)
            {
                sb.Append(char.ToUpperInvariant(c));
                capitalizeNext = false;
            }
            else
            {
                sb.Append(c);
            }
        }

        return sb.ToString();
    }

    private static string ToCSharpIdentifier(string name)
    {
        var sb = new System.Text.StringBuilder();
        var capitalizeNext = true;

        foreach (var c in name)
        {
            if (c == '_' || c == '-' || c == ' ')
            {
                capitalizeNext = true;
            }
            else if (char.IsLetterOrDigit(c))
            {
                if (capitalizeNext)
                {
                    sb.Append(char.ToUpperInvariant(c));
                    capitalizeNext = false;
                }
                else
                {
                    sb.Append(c);
                }
            }
        }

        var result = sb.ToString();

        // Ensure identifier doesn't start with a digit
        if (result.Length > 0 && char.IsDigit(result[0]))
            result = "_" + result;

        // Handle empty result
        if (string.IsNullOrEmpty(result))
            result = "_";

        return result;
    }
}
